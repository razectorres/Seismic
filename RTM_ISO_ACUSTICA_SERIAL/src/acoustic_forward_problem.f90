subroutine acoustic_forward_problem(P1,P2,P3,A,C,vel2,fonte,Gs,Gi,Ge,Gd,shot_loop,PDir,Pcor)	
  use parameters  
  implicit none
  !***********************************************************************
  !**Variaveis da equacao da onda*****************************************
  !***********************************************************************  
  real,dimension (Nxx,Nzz),intent(inout) :: P1		! Campo em t-dt
  real,dimension (Nxx,Nzz),intent(inout) :: P2		! Campo em t
  real,dimension (Nxx,Nzz),intent(inout) :: P3 	! Campo em t+dt 
  !***********************************************************************
  !**Variaveis auxiliares*************************************************
  !***********************************************************************  	     
  integer :: is,js,t 
  !***********************************************************************
  !**Modelo de velocidade*************************************************
  !***********************************************************************
  real,dimension (Nxx,Nzz),intent(inout)  :: A
  real,dimension (Nxx,Nzz),intent(inout)  :: C
  real,dimension (Nxx,Nzz),intent(inout)  :: vel2
  !***********************************************************************
  !**Perfil de amortecimento da camada de cerjan**************************
  !***********************************************************************      
  real,dimension (nzbs+1),intent(in)   :: Gs		! fator de amortec.
  real,dimension (nzbi+1),intent(in)   :: Gi		! fator de amortec.
  real,dimension (nxbe+1),intent(in)   :: Ge		! fator de amortec.
  real,dimension (nxbd+1),intent(in)   :: Gd		! coef. de amortec.

  !***********************************************************************
  !**Parametros da fonte: Ricker de fase zero ****************************
  !*********************************************************************** 
  real,dimension (Nf),intent(in)   :: fonte	! fonte

  !***********************************************************************
  !**Dado observado******************************************************
  !*********************************************************************** 
  real,dimension (Nxx,Nzz,Ntotal),intent(inout) :: Pdir 
  real,dimension (Nxx,Nzz),intent(inout)        :: Pcor  

  integer,intent(in) :: shot_loop
  character(len=3)   :: varsnap
  integer :: k_snap,dt_snap 
  k_snap=0
  dt_snap=250

  !***********************************************************************
  !*****Inicio do loop de tiros*******************************************
  !*********************************************************************** 
	pti=ishot+(shot_loop-1)*dshot
	! write(6,19)
	! write(6,20) shot_loop,Nshot
	! write(6,21)			
	! 19	format(/,15x,"===============================================")
	! 20 	format(30x,"tiro",I3,1x,"-",I3)
	! 21	format(15x,"==============================================="/)						
  !***********************************************************************
  !********Inicializacao**************************************************
  !***********************************************************************
	P1 = 0.0 ; P2 = 0.0 ; P3 = 0.0;
  !***********************************************************************
  !********Inicio do loop temporal****************************************
  !***********************************************************************
	do n = 1, Ntotal
    !***********************************************************************
    !***********Termo Fonte*************************************************
    !***********************************************************************
	  if (n.le.Nf2) then
	  	P2(pti,ptj) = P2(pti,ptj) + fonte(n)*((vel2(pti,ptj)*dt)**2)
	  endif					
    !***********************************************************************
    !***********Equacao da Onda*********************************************
    !***********************************************************************					
	  call wave_equation(P1,P2,P3,C)					
    !***********************************************************************
    !***********Bordas de Reynolds******************************************
    !***********************************************************************
	  call Reynolds(A,P2,P3)		
    !***********************************************************************
    !***********Bordas de Absorcao - Cerjan*********************************
    !***********************************************************************		
	  call cerjan(Gs,Gi,Ge,Gd,P2,P3)		
    !***********************************************************************
    !***********snaps*******************************************************
    !***********************************************************************
    !IF (mod(n,dt_snap).eq.0) THEN
    !   write(varsnap,'(i3.3)')k_snap
    !   write(*,*)"snapshot = ",varsnap
    !   open(unit=10,file="./outputs/forw"//varsnap//".bin",status="replace",access="direct",form="unformatted",recl=4*Nx*Nz)
    !   write(10,rec=1)((P3(is,js),js=jybs,jybi),is=ixbe,ixbd)     
    !   close(10)
    !   k_snap=k_snap + 1    
    !END IF 
    !***********************************************************************
	  !***********Salvando o campo de onda direto*****************************
	  !***********************************************************************
	    if (mod(n,sampling).eq.0) then
        Do js=jybs,jybi
         Do is=ixbe,ixbd
            PDir(is,js,(n/sampling))=P3(is,js)
         end do
        end do
      endif    
 
      Do js=jybs,jybi
        Do is=ixbe,ixbd
           Pcor(is,js)=Pcor(is,js)+P3(is,js)*P3(is,js)
        end do
      end do
    !***********************************************************************
		!***********Atualizacao do campo de onda********************************
		!***********************************************************************
    P1 = P2
    P2 = P3
    !***********************************************************************
    !***********Contador de passos de tempo*********************************
    !***********************************************************************
	   if (mod(n,1000).eq.0)then
	   	write(6,22)	n
	   	22  format(36x,I5)	        						    
	   end if
    !***********************************************************************
    !********Fechamento do loop temporal************************************
    !*********************************************************************** 
  end do

		
	
end subroutine acoustic_forward_problem
